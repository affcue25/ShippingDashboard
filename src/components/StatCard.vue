  }
})

const iconComponent = computed(() => {
  const icons = {
    truck: 'TruckIcon',
    cube: 'CubeIcon',
    scale: 'ScaleIcon',
    location: 'LocationIcon',
    user: 'UserIcon',
    document: 'DocumentIcon',
